// -----------------------------------------------------------------------------

Future<void> _generateLicenseHeaders(String template) async {
  await generateLicenseHeadersApp([
    "-t",
    "$currentScriptDir/templates/generate_license_headers/$template",
    "-r",
    folders
        .map((e) => "$currentScriptDir/../${e.isNotEmpty ? "$e/" : ""}")
        .join("&"),
  ]);
}
