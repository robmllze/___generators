void main() async {
  for (final folder in folders) {
    await generateScreenBindingsApp([
      '-t',
      '$currentScriptDir/templates/generate_screen_bindings/your_screen_bindings_template.dart.md',
      '-r',
      "$currentScriptDir/../${folder.isNotEmpty ? "$folder/" : ""}",
      '-s',
      subfolders.join('&'),
    ]);
  }
}
